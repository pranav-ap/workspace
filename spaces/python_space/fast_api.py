import uvicorn
from typing import Optional, Annotated
from pydantic import BaseModel
from fastapi import FastAPI, Query, Depends


class Item(BaseModel):
    name: str
    description: Optional[str] = None
    price: float
    tax: Optional[float] = None


app = FastAPI()


@app.get("/")
async def index():
    return {"message": "Welcome to the Iris classification API. Use `/predict` to classify a flower."}


@app.get("/items/{item_id}")
async def read_item(item_id: int):
    return {"item_id": item_id}


@app.post("/items/")
async def create_item(item: Item):
    item_dict = item.dict()
    if item.tax:
        price_with_tax = item.price + item.tax
        item_dict.update({"price_with_tax": price_with_tax})
    return item_dict


@app.get("/items/")
async def read_items(q: Annotated[str | None, Query(max_length=50)] = None):
    results = {"items": [{"item_id": "Foo"}, {"item_id": "Bar"}]}
    if q:
        results.update({"q": q})
    return results


async def common_parameters(q: str | None = None, skip: int = 0, limit: int = 100):
    return {"q": q, "skip": skip, "limit": limit}


@app.get("/items/")
async def read_items2(commons: Annotated[dict, Depends(common_parameters)]):
    # common_parameters returns dict, so we have dict above as first param
    return commons


if __name__ == "__main__":
    uvicorn.run(app, host="0.0.0.0", port=8000)
